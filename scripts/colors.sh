#!/bin/bash

# Function to print in black
print_black() {
    echo -e "\e[30m$1\e[0m"
}

# Function to print in red
print_red() {
    echo -e "\e[31m$1\e[0m"
}

# Function to print in green
print_green() {
    echo -e "\e[32m$1\e[0m"
}

# Function to print in yellow
print_yellow() {
    echo -e "\e[33m$1\e[0m"
}

# Function to print in blue
print_blue() {
    echo -e "\e[34m$1\e[0m"
}

# Function to print in magenta
print_magenta() {
    echo -e "\e[35m$1\e[0m"
}

# Function to print in cyan
print_cyan() {
    echo -e "\e[36m$1\e[0m"
}

# Function to print in white
print_white() {
    echo -e "\e[37m$1\e[0m"
}

# Function to print in bold black
print_bold_black() {
    echo -e "\e[1;30m$1\e[0m"
